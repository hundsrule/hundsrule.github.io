    for line in question_part.split('\n'):
        if re.match(r'^\d+\..*', line):  # 匹配章节标题
            current_section = line.strip()
        elif re.match(r'^\(\d+\)', line):  # 匹配问题
            questions.append((current_section, line.strip()))
            
    # 提取所有答案
    answers = []
    current_section = ""
    for line in answer_part.split('\n'):
        if re.match(r'^\d+\..*', line):
            current_section = line.strip()
        elif re.match(r'^\(\d+\)', line):
            answers.append((current_section, line.strip()))
            
    # 将问题和答案配对
